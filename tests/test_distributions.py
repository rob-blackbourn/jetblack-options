def test_cnd():
    actual = CND(1.23564285010596)
    expected = 0.89170432529243
    assert is_close_to(actual, expected, 1e-6)


def test_nd():
    actual = ND(1.23564285010596)
    expected = 0.1859374114061063
    assert is_close_to(actual, expected, 1e-6)


def test_cndev():
    actual = CNDEV(0.7)
    expected = 0.5244005119066525
    assert is_close_to(actual, expected, 1e-6)


def test_cdf():
    nd = NormalDist()
    actual = nd.cdf(1.23564285010596)
    expected = 0.89170432529243
    assert is_close_to(actual, expected, 1e-6)


def test_pdf():
    nd = NormalDist()
    actual = nd.pdf(1.23564285010596)
    expected = 0.1859374114061063
    assert is_close_to(actual, expected, 1e-6)


def test_inv_cdf():
    nd = NormalDist()
    actual = nd.inv_cdf(0.7)
    expected = 0.5244005119066525
    assert is_close_to(actual, expected, 1e-6)


def test_chi_inv():
    actual = CHIINV(0.1, 5)
    expected = 9.184520236568392
    assert is_close_to(actual, expected, 1e-12)

    actual = CHIINV(0.1, 10)
    expected = 15.937814138474298
    assert is_close_to(actual, expected, 1e-12)

    actual = CHIINV(0.7, 5)
    expected = 3.0123268283837
    assert is_close_to(actual, expected, 1e-12)

    actual = CHIINV(0.7, 10)
    expected = 7.356372548526449
    assert is_close_to(actual, expected, 1e-12)


